package assignment;

public class PractiseIfStatement {

	public static void main(String[] args) {
		int a=13;
		if(a%2==0) {
			System.out.println("even");
		}
		else {
			System.out.println("odd");
		}
		
		
		int num1=20, num2=30, num3=46;
		double num4=(num1+num2+num3)/3;
		if(num4%2==0) {
			System.out.println("even");
		}
		else {
			System.out.println("odd");
		}
		

	}

}
